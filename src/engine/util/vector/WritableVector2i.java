package engine.util.vector;

public interface WritableVector2i {

    /**
     * Set the X value
     * @param x
     */
    void setX(int x);

    /**
     * Set the Y value
     * @param y
     */
    void setY(int y);

    /**
     * Set the X,Y values
     * @param x
     * @param y
     */
    void set(int x, int y);

}